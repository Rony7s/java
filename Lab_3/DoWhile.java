package Lab_3;

public class DoWhile {

    public static void main(String[] args) {

        int i = 0;
        System.out.println("Printing the list of first 10 even numbers \n");
        do {
            System.out.println(i);
            i = i + 2;
        } while (i <= 10);
    }

}
