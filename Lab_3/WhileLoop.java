package Lab_3;

public class WhileLoop {

    public static void main(String[] args) {

        int i = 0;
        System.out.println("Printing the list of first 10 even numbers \n");
        while (i <= 10) {
            System.out.println(i);
            i = i + 2;
        }
    }

}
